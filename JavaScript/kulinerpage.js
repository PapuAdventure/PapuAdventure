var detail1 = document.getElementById("asap");
var opnbtn1 = document.getElementById("asapOpen");
var clsbtn1 = document.getElementById("asapClose");

opnbtn1.onclick = function(){
    detail1.style.display = "block";
}

clsbtn1.onclick = function(){
    detail1.style.display = "none";
} 

window.onclick = function(event) {
    if (event.target == detail1) {
      detail1.style.display = "none";
    }
}

